public class Thing {
    private String id;
    private String type;
    private String color;
    private int count;

    public String getId() {
        return id;
    }

    public void setId(String _id) {
        id = _id;
    }

    public String getType() {
        return type;
    }

    public void setType(String _type) {
        type = _type;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String _color) {
        color = _color;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int _count) {
        count = _count;
    }
}
